                SELECT TEST_ONE + TEST_TWO AS TEST_SUM, ID, NOTICE

                FROM OLD_TEST;


        BEGIN

            OPEN C1; --커서를 OPEN 시켜준다는 의미

            LOOP

                FETCH C1 INTO TEST_SUM,ID,NOTICE; --커서를 이런식의 타입으로 가져오는거 같음

                EXIT WHEN C1%NOTFOUND; --존재하지 않으면 LOOP 닫음.

                INSERT INTO NEW_TEST (TEST_SUM, ID, NOTICE) VALUES (TEST_SUM, ID, NOTICE);

            END LOOP;

            CLOSE C1;

        END;


